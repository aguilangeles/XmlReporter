    private void fileExist(String name) {
        AcondicionarFiles getfiles = new AcondicionarFiles(name);
        if (getfiles.isFile()) {
            File folders = getfiles.getFolder();
            File[] listofFiles = folders.listFiles();
            GetDirectorios dirs = new GetDirectorios(name, getfiles.getFilefilter(), getfiles.getFolder());
            List<String> lista = new ControlaExistenciaXML(dirs).getLista();
            boolean procesoviable = (lista.isEmpty()) ? true : false;
            if (procesoviable) {
                reporte = new MyWorker(infoJLabel, jTRuta, dirs);
                this.reporte.execute();
            } else {
                cerrarPrograma(lista);
            }
        } else {
            archivoNoEncontrado();
        }
    }
