    private void cerrarPrograma(List<String> lista) throws HeadlessException {
        Logger.getLogger(Log.class.getName()).log(Level.SEVERE, "Este programa no tiene xml", "hola, soy un mensaje");
        String lineas = (lista.toString().replace(", ", "\n"));
        String sincorchetes = lineas.substring(1, lineas.length() - 1);
        String mensaje = "No puede llevarse a cabo el proceso"
                + " porque los siguientes archivos: \n"
                + sincorchetes
                + "\nNo se encuentran.\nEl programa se cerrará.";
        ExceptionMessage.message(mensaje, Log.class.getName());
        System.exit(0);
    }

    private void archivoNoEncontrado() throws HeadlessException {
        ExceptionMessage.message(AcondicionarFiles.FILENOTFOUND, Log.class.getName());
        jTRuta.setText("");
    }
